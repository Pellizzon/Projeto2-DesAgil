package br.edu.insper.al.matheusp1.projeto2.ui.login;

class Dados {

    private String nome;
    private String valor;

    public Dados(String nome, String valor) {
        this.nome = nome;
        this.valor = valor;
    }

    public String getNome() {
        return nome;
    }

    public String getValor() {
        return valor;
    }
}
